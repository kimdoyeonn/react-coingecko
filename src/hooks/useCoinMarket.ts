const useCoinMarket = ({
  currency,
  perPage = 100,
  view = 'all',
}: {
  currency: CurrencyType
  perPage?: number | string
  view: 'all' | 'bookmark'
}) => {
  const [coins, setCoins] = useState<CoinType[]>([] as CoinType[])
  const [isLoading, setIsLoading] = useState<boolean>(false)
  const { bookmarks } = useBookmark()
  const [page, setPage] = useState(1)

  const fetchMore = () => {
    setPage((prev) => prev + 1)
  }

  useEffect(() => {
    const fetchCoinMarkets = async () => {
      try {
        const coins = await Coin.getMarkets({
          currency,
          perPage: Number(perPage),
          page,
          ...(view === 'bookmark' ? { ids: bookmarks.join(',') } : {}),
        })
        if (page === 1) {
          setCoins(coins.data)
        } else {
          setCoins((prev) => [...prev, ...coins.data])
        }
      } catch (err) {
        console.error(err)
      } finally {
        setIsLoading(false)
      }
    }
    fetchCoinMarkets()
    return () => {
      setIsLoading(true)
    }
  }, [view, currency, perPage, page])

  return { coins, isLoading, fetchMore }
}

export default useCoinMarket
